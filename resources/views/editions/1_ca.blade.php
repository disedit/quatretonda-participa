<section aria-labelledby="about-1" class="about-block" aria-live="polite">
    <h3 id="about-1">Què és #QuatretondaParticipa?</h3>
    <img src="/images/choice-2.svg" class="about-block__thumbnail" alt="" aria-hidden="true" />

    <p>El Govern de Quatretonda vol aprofundir en la regeneració democràtica i la participació. Regeneració democràtica que passa per donar veu als ciutadans i ciutadanes i acostar-los a la presa de decisions. Per això, en aquest exercici la ciutadania de Quatretonda podrà proposar els projectes d'inversions que estimen més oportuns i convenients i després decidir la prioritat de cadascun amb una consulta ciutadana.</p>

    <p>En la primera part, la ciutadania de Quatretonda podrà enviar, mitjançant un formulari habilitat en el web, les seues propostes per a projectes d'inversions que creguen convenients. </p>

    <p>Una vegada rebuts i esgotat el termini, els tècnics de l'Ajuntament destriaran quins projectes són de competència municipal i quins no, així com quines propostes es poden dur endavant i quines no, i en donarà forma per tal de presentar-les al procés de votació. Es publicarà un document amb les propostes desestimades i les raons per les quals ho han estat. </p>

    <p>Finalment, la ciutadania de Quatretonda podrà triar i ordenar per prioritats entre els projectes sotmesos finalment a votació mitjançant un procés telemàtic que es vehicularà a través d'aquesta web.</p>

    <p>Tots i totes proposem i decidim les qüestions que afecten al nostre poble, tots i totes fem sentir la nostra veu!</p>
</section>

